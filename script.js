document.addEventListener('DOMContentLoaded', () => {
    // Current year in footer
    document.getElementById('year').textContent = new Date().getFullYear();

    // Custom Language Dropdown handling
    const langDropdownBtn = document.getElementById('langDropdownBtn');
    const langDropdownContent = document.getElementById('langDropdownContent');
    const langOptions = document.querySelectorAll('.lang-option');

    // Check localStorage for saved lang, default to 'en'
    const savedLang = localStorage.getItem('fizz_promo_lang') || 'en';
    setLanguage(savedLang);
    updateDropdownUI(savedLang);

    langDropdownBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdownContent.classList.toggle('show');
    });

    // Close dropdown when clicking outside
    window.addEventListener('click', () => {
        if (langDropdownContent.classList.contains('show')) {
            langDropdownContent.classList.remove('show');
        }
    });

    langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
            const lang = option.getAttribute('data-value');
            setLanguage(lang);
            updateDropdownUI(lang);
            localStorage.setItem('fizz_promo_lang', lang);
            langDropdownContent.classList.remove('show');
        });
    });

    function updateDropdownUI(lang) {
        const selectedOption = document.querySelector(`.lang-option[data-value="${lang}"]`);
        if (selectedOption) {
            langDropdownBtn.innerHTML = selectedOption.innerHTML;
        }
    }

    // Copy to clipboard functionality
    const copyBtn = document.getElementById('copyBtn');
    const refCode = document.getElementById('refCode');
    const copyMsg = document.getElementById('copyMsg');

    copyBtn.addEventListener('click', async () => {
        const codeText = refCode.textContent;
        try {
            await navigator.clipboard.writeText(codeText);
            copyMsg.classList.add('show');
            setTimeout(() => {
                copyMsg.classList.remove('show');
            }, 2000);
        } catch (err) {
            console.error('Failed to copy text: ', err);
            // Fallback for older browsers
            const textArea = document.createElement("textarea");
            textArea.value = codeText;
            document.body.appendChild(textArea);
            textArea.select();
            document.execCommand("Copy");
            textArea.remove();
            copyMsg.classList.add('show');
            setTimeout(() => {
                copyMsg.classList.remove('show');
            }, 2000);
        }
    });

    // Accordion FAQ functionality
    const accordionHeaders = document.querySelectorAll('.accordion-header');
    accordionHeaders.forEach(header => {
        header.addEventListener('click', () => {
            header.classList.toggle('active');
        });
    });
});

function setLanguage(lang) {
    const dict = translations[lang];
    if (!dict) return;

    // Set text direction for Arabic
    document.documentElement.setAttribute('dir', dict.dir);
    document.documentElement.setAttribute('lang', lang);

    // Update title
    document.title = dict.title;

    // Update meta description safely
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) {
        metaDesc.setAttribute("content", dict.description);
    }

    // Update Meta Keywords
    const metaKeywords = document.querySelector('meta[name="keywords"]');
    if (metaKeywords && dict.keywords) {
        metaKeywords.setAttribute("content", dict.keywords);
    }

    // Update Open Graph Tags
    const ogTitle = document.querySelector('meta[property="og:title"]');
    if (ogTitle && dict.og_title) {
        ogTitle.setAttribute("content", dict.og_title);
    }

    const ogDesc = document.querySelector('meta[property="og:description"]');
    if (ogDesc && dict.og_description) {
        ogDesc.setAttribute("content", dict.og_description);
    }

    // Update DOM elements
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.innerHTML = dict[key]; // Allow HTML inside strings (like <strong>)
        }
    });
}
